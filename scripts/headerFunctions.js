// Function to toggle visibility on click and hover
function toggleVisibilityOnClick(button, container) {
    if (!button || !container) return;
    
    // Set initial state
    container.style.visibility = "hidden";
    
    button.addEventListener("click", (e) => {
        e.stopPropagation(); // Prevent document click from immediately hiding
        const isVisible = container.style.visibility === "visible";
        container.style.visibility = isVisible ? "hidden" : "visible";
    });

    container.addEventListener("mouseover", () => {
        container.style.visibility = "visible";
    });
 
    container.addEventListener("mouseout", () => {
        container.style.visibility = "hidden";
    });
}

// Function to update page language
function updateLanguage(language) {
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[language] && translations[language][key]) {
            // Use innerHTML for specific elements that contain HTML markup
            if (key === 'fullGuidelines') {
                element.innerHTML = translations[language][key];
            } else {
                element.textContent = translations[language][key];
            }
        }
    });

    // Save language preference
    localStorage.setItem('preferredLanguage', language);
}

// Function to setup language switching
function setupLanguageSwitching() {
    const languageSpans = {
        english: document.getElementById('english'),
        spanish: document.getElementById('spanish'),
        russian: document.getElementById('russian')
    };

    // Add click handlers for language switching
    Object.keys(languageSpans).forEach(lang => {
        if (languageSpans[lang]) {
            languageSpans[lang].addEventListener('click', () => {
                updateLanguage(lang);
                // Hide language container after selection
                document.querySelector('.language-container').style.visibility = 'hidden';
            });
        }
    });

    // Load saved language preference or default to English
    const savedLanguage = localStorage.getItem('preferredLanguage') || 'english';
    updateLanguage(savedLanguage);
}

